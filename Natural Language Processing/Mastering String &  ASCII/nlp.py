def capitalize(text):
    return text.capitalize()

def to_upper(text):
    return text.upper()

def to_lower(text):
    return text.lower()

def is_title(text):
    return text.istitle()

def is_digit(text):
    return text.isdigit()

def is_alpha(text):
    return text.isalpha()

def is_numeric(text):
    return text.isnumeric()